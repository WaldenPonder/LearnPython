from typing import List


class Solution:
    def compress(self, chars: List[str]) -> int:
        cnt, i, k = 0, 0, 0

        while i < len(chars):
            c = chars[i]
            j = i + 1
            tmp = 1
            while j < len(chars) and c == chars[j]:
                j += 1
                i += 1
                tmp += 1
            if tmp == 1:
                cnt += 1
                chars[k] = c
                k += 1
            else:
                cnt += (len(str(j)) + 1)
                chars[k] = c
                k += 1
                for cc in str(tmp):
                    chars[k] = cc
                    k += 1
            i += 1
        chars = chars[:k]
        print(chars)
        return cnt


